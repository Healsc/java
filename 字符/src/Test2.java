public class Test2 {
    public static void main(String[] args){
        System.out.println(true&false);//与  false
        System.out.println(true|false);//或  true
        System.out.println(true^false);//异或 true
        System.out.println(false^false);//异或 false
        System.out.println(true&&false);//短路与false
        System.out.println(false||true);//短路或true
    }
}
